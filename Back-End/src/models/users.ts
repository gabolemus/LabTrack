import { model, Schema } from "mongoose";
import { IUser } from "../types/user";

/** Mongoose schema for users */
const userSchema: Schema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    salt: {
      type: String,
      required: true,
    },
    passwordHash: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      required: true,
    },
  },
  { timestamps: true },
);

export default model<IUser>("User", userSchema);
